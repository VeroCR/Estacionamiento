    public class reporte extends Thread{
        Socket cliente;
        int puerto = 9000;
        String ip = "127.0.0.1";
        
        @Override
        public void run(){
            ServerSocket servidor = null;
            Socket sc = null;
            DataInputStream in;
            
            try{
                //socket del servidor
                servidor = new ServerSocket (puerto);
                
                //Siempre estará escuchando las peticiones
                while(true){
                    sc = servidor.accept();
                    in = new DataInputStream(sc.getInputStream());
                    
                    //Leer el mensaje que se envía
                    String mensaje = in.readUTF();
                    jTextArea1.setText(mensaje);
                    System.out.println("Reporte recibido");
                    
                    //Cerrar socket
                    sc.close();
                }
            } catch (IOException ex) {
                Logger.getLogger(AutorizarNomina.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }
    
